#!/usr/bin/env bash
set -e

WC_VERSION=${WC_VERSION:-9.9.5}
WP_VERSION=${WP_VERSION:-6.8.5}
WP_TESTS_DIR=${WP_TESTS_DIR:-/tmp/wordpress-tests-lib}
WP_CORE_DIR=${WP_CORE_DIR:-/tmp/wordpress}
DB_HOST="woocommerce_test_db"
DB_NAME="woocommerce_tests"
DB_USER="root"
DB_PASS="woocommerce_test_password"

echo "==> Waiting for MySQL at $DB_HOST..."
until mysql -h "$DB_HOST" -u "$DB_USER" -p"$DB_PASS" --skip-ssl -e "SELECT 1" 2>/dev/null; do
    sleep 2
done
echo "==> MySQL ready."

if [ ! -f "$WP_TESTS_DIR/includes/functions.php" ] || [ ! -f "$WP_CORE_DIR/wp-settings.php" ]; then
    echo "==> Installing WordPress test library (WP $WP_VERSION)..."
    # Remove incomplete cache contents so install-wp-tests.sh re-exports SVN content.
    rm -rf "$WP_TESTS_DIR"/{includes,data,wp-tests-config.php} 2>/dev/null || true
    /project/bin/install-wp-tests.sh "$DB_NAME" "$DB_USER" "$DB_PASS" "$DB_HOST" "$WP_VERSION" true
    echo "==> WordPress test library ready."
fi

setup_woocommerce_monorepo() {
    local wc_plugin_dir="/project/woocommerce/plugins/woocommerce"

    # Helper: fetch test suite from GitHub if tests/ dir is missing.
    fetch_wc_tests() {
        if [ -d "$wc_plugin_dir/tests" ]; then
            return 0
        fi
        echo "==> Fetching WooCommerce test suite from GitHub (sparse clone)..."
        local wc_test_clone="/tmp/wc-tests-clone"
        rm -rf "$wc_test_clone"
        git clone --depth 1 --branch "$WC_VERSION" \
            --filter=blob:none \
            --sparse \
            https://github.com/woocommerce/woocommerce.git "$wc_test_clone"
        (
            cd "$wc_test_clone"
            git sparse-checkout set "plugins/woocommerce/tests"
        )
        mv "$wc_test_clone/plugins/woocommerce/tests" "$wc_plugin_dir/tests"
        rm -rf "$wc_test_clone"
        # Add phpunit-polyfills (not in SVN vendor, needed by WC test bootstrap).
        if [ ! -d "$wc_plugin_dir/vendor/yoast/phpunit-polyfills" ]; then
            mkdir -p /tmp/polyfills-extract
            curl -sL "https://github.com/Yoast/PHPUnit-Polyfills/archive/refs/tags/2.0.1.tar.gz" \
                -o /tmp/polyfills.tar.gz
            tar xzf /tmp/polyfills.tar.gz -C /tmp/polyfills-extract
            mkdir -p "$wc_plugin_dir/vendor/yoast"
            mv /tmp/polyfills-extract/PHPUnit-Polyfills-2.0.1 \
                "$wc_plugin_dir/vendor/yoast/phpunit-polyfills"
            rm -rf /tmp/polyfills-extract /tmp/polyfills.tar.gz
        fi
        # Create stub build artifact required by WC test bootstrap (normally from pnpm build).
        if [ ! -f "$wc_plugin_dir/client/admin/config/development.json" ]; then
            mkdir -p "$wc_plugin_dir/client/admin/config"
            echo '{"features":{}}' > "$wc_plugin_dir/client/admin/config/development.json"
        fi
    }

    if [ -f "$wc_plugin_dir/woocommerce.php" ] && [ -d "$wc_plugin_dir/vendor" ]; then
        # Plugin and vendor exist — only fetch tests if missing.
        fetch_wc_tests
        return 0
    fi
    # Download WooCommerce from WordPress.org SVN — includes both built assets
    # (e.g. includes/react-admin/feature-config.php) and vendor/ dependencies.
    if [ -f "$wc_plugin_dir/woocommerce.php" ] && [ ! -d "$wc_plugin_dir/vendor" ]; then
        echo "==> Fetching WooCommerce $WC_VERSION vendor via SVN export (assets present, vendor missing)..."
        svn export --quiet "https://plugins.svn.wordpress.org/woocommerce/tags/${WC_VERSION}/vendor" \
            "$wc_plugin_dir/vendor"
        fetch_wc_tests
        return 0
    fi
    echo "==> Installing WooCommerce $WC_VERSION from WordPress.org SVN..."
    mkdir -p /project/woocommerce/plugins
    svn export --quiet "https://plugins.svn.wordpress.org/woocommerce/tags/${WC_VERSION}" \
        "$wc_plugin_dir"
    fetch_wc_tests
    # Create stub build artifact required by WC test bootstrap (normally generated by pnpm build).
    mkdir -p "$wc_plugin_dir/client/admin/config"
    echo '{"features":{}}' > "$wc_plugin_dir/client/admin/config/development.json"
    echo "==> WooCommerce ready."
}

setup_woocommerce_standalone() {
    local wc_dir="/project/woocommerce"
    if [ -f "$wc_dir/woocommerce.php" ]; then
        return 0
    fi
    echo "==> Downloading WooCommerce $WC_VERSION (standalone)..."
    local tmpfile
    tmpfile=$(mktemp /tmp/wc-XXXXXX.tar.gz)
    curl -sL "https://github.com/woocommerce/woocommerce/archive/refs/tags/${WC_VERSION}.tar.gz" -o "$tmpfile"
    mkdir -p /tmp/wc-extract
    tar xzf "$tmpfile" -C /tmp/wc-extract
    local extracted_dir
    extracted_dir=$(ls /tmp/wc-extract/)
    mv "/tmp/wc-extract/$extracted_dir"/* "$wc_dir/"
    rm -f "$tmpfile"
    rm -rf /tmp/wc-extract
    if [ -f "$wc_dir/composer.json" ]; then
        echo "==> Running composer install for WooCommerce..."
        composer install --working-dir="$wc_dir" --no-dev --prefer-dist --no-interaction --no-progress
    fi
    echo "==> WooCommerce ready."
}

monorepo_wc_php="/project/woocommerce/plugins/woocommerce/woocommerce.php"
monorepo_wc_vendor="/project/woocommerce/plugins/woocommerce/vendor"
monorepo_wc_tests="/project/woocommerce/plugins/woocommerce/tests"
standalone_wc_php="/project/woocommerce/woocommerce.php"

wc_monorepo_complete() {
    [ -f "$monorepo_wc_php" ] && [ -d "$monorepo_wc_vendor" ] && [ -d "$monorepo_wc_tests" ]
}

if wc_monorepo_complete; then
    : # WooCommerce monorepo install is complete, nothing to do.
elif [ -f "$monorepo_wc_php" ]; then
    # Partial monorepo install — run setup to complete it.
    setup_woocommerce_monorepo
elif [ ! -f "$standalone_wc_php" ]; then
    # No WooCommerce at all — install via monorepo setup (uses SVN release + GitHub tests).
    setup_woocommerce_monorepo
fi

cd /project
exec vendor/bin/phpunit -c tests/phpunit.dist.xml "$@"
